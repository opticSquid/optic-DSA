class Solution
{
private:
    vector<int> del_row = {-1, 0, +1, 0};
    vector<int> del_col = {0, +1, 0, -1};
    void bfs(vector<vector<int>> &grid, int n, vector<vector<int>> &score_card)
    {
        queue<pair<int, int>> q;
        for (int i = 0; i < n; i++)
        {
            for (int j = 0; j < n; j++)
            {
                // if thief
                if (grid[i][j])
                {
                    // make their score 0
                    score_card[i][j] = 0;
                    // add them to q
                    q.push({i, j});
                }
            }
        }
        while (!q.empty())
        {
            pair<int, int> node = q.front();
            int score = score_card[node.first][node.second];
            q.pop();
            for (int i = 0; i < 4; i++)
            {
                int n_row = node.first + del_row[i];
                int n_col = node.second + del_col[i];
                // bounding box + check if score s higher tan what it should be
                if (n_row >= 0 && n_row < n && n_col >= 0 && n_col < n && score_card[n_row][n_col] > 1 + score)
                {
                    // find the minimum score of the cell from any of the theif
                    score_card[n_row][n_col] = 1 + score;
                    q.push({n_row, n_col});
                }
            }
        }
    }

public:
    int maximumSafenessFactor(vector<vector<int>> &grid)
    {
        int n = grid.size();
        // boundary condition
        if (grid[0][0] || grid[n - 1][n - 1])
        {
            return 0;
        }
        vector<vector<int>> safety_score(n, vector<int>(n, INT_MAX));
        bfs(grid, n, safety_score);
        // djiktra's traversal
        priority_queue<pair<int, pair<int, int>>> pq;
        vector<vector<int>> visited(n, vector<int>(n, false));
        pq.push({safety_score[0][0], {0, 0}});
        while (!pq.empty())
        {
            pair<int, pair<int, int>> cell = pq.top();
            pq.pop();
            if (cell.second.first == n - 1 && cell.second.second == n - 1)
            {
                return cell.first;
            }
            visited[cell.second.first][cell.second.second] = 1;
            for (int i = 0; i < 4; i++)
            {
                int n_row = cell.second.first + del_row[i];
                int n_col = cell.second.second + del_col[i];
                // bounding box + don't check theives
                if (n_row >= 0 && n_row < n && n_col >= 0 && n_col < n && !visited[n_row][n_col])
                {
                    int curr_safety = min(cell.first, safety_score[n_row][n_col]);
                    pq.push({curr_safety, {n_row, n_col}});
                    // I don't know why
                    visited[n_row][n_col] = true;
                }
            }
        }
        return -1;
    }
};
